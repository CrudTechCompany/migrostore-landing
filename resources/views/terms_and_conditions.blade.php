<div>
    <span>§ 1</span>
    <div>Definicje</div>
    <p>
        „Formularz rejestracyjny” – formularz wypełniany przez
        Użytkownika niezalogowanego, służący do założenia Konta.
    </p>
    <p>
        „Konto” – zbiór zasobów i ustawień utworzonych dla Użytkownika w
        ramach Serwisów. Użytkownik może wykorzystywać Konto do
        zarządzania Usługami.
    </p>
    <p>
        „Kodeks Cywilny” – ustawa z dnia 23 kwietnia 1964 r. Kodeks
        cywilny (Dz.U. 1964 nr 16 poz. 93).
    </p>
    <p>
        „Profil” – funkcjonalność Konta pozwalająca Użytkownikowi na
        gromadzenie wybranych informacji, w tym dotyczących przebiegu
        jego kariery zawodowej, historii edukacji i innych umiejętności
        oraz dokumentów Użytkownika.
    </p>
    <p>„Regulamin” – niniejszy Regulamin.</p>
    <p>
        „Serwisy” lub „Serwis, aplikacja lub platforma” – serwisy
        internetowe należące do Usługodawcy, w ramach których
        Usługodawca świadczy Usługi.
    </p>
    <p>
        „Usługi” – wszelkie usługi świadczone drogą elektroniczną przez
        Usługodawcę na rzecz Użytkowników i Użytkowników niezalogowanych
        w oparciu o Regulamin.
    </p>
    <p>
        „Usługodawca” – Migrostore Sp. z o. o. z siedzibą we Wroclawiu,
        al. Aleksandra Ostrowskiego 13, 53-238, wpisana do rejestru
        przedsiębiorców prowadzonego przez Sąd Rejonowy we Wroclaw, VI
        Wydział Gospodarczy Krajowego Rejestru Sądowego pod nr KRS
        0000966678, posiadająca numer identyfikacji podatkowej NIP
        8943184905.
    </p>
    <p>
        „Ustawa o prawie autorskim i prawach pokrewnych” – ustawa z dnia
        4 lutego 1994 r. o prawie autorskim i prawach pokrewnych (Dz.U.
        1994 nr 24 poz. 83).
    </p>
    <p>
        „Ustawa o świadczeniu usług drogą elektroniczną” – ustawa z dnia
        18 lipca 2002 r. o świadczeniu usług drogą elektroniczną (Dz.U.
        2002 nr 144 poz. 1204).
    </p>
    <p>
        „Ustawienia (Prywatność)” – funkcjonalność Konta pozwalająca
        Użytkownikowi korzystającemu z wybranych Usług na odpowiednie
        zarządzanie tymi Usługami, w tym na samodzielne modyfikowanie
        ich zakresu oraz na wybór preferencji odnośnie ochrony jego
        prywatności.
    </p>
    <p>
        „Utwory” – utwory w rozumieniu Ustawy o prawie autorskim i
        prawach pokrewnych, które będą publikowane przez Użytkownika w
        ramach korzystania z Serwisów lub wybranych Usług.
    </p>
    <p>
        „Uwierzytelnienie” – usługa uwierzytelniająca polegająca na
        wysłaniu kodu za pośrednictwem sms lub email, umożliwiająca
        założenie Konta i uzyskanie do niego dostępu.
    </p>
    <p>
        „Użytkownik” – pełnoletnia osoba fizyczna, która posiada Konto i
        korzysta z Usług świadczonych w ramach Serwisów lub Pracodawcy
        lub agencje doradztwa personalnego, którzy publikują ogłoszenia
        o pracę w Serwisie lub korzystają z innych usług realizowanych
        przez Usługodawcę.
    </p>
    <p>
        „Użytkownik niezalogowany” – pełnoletnia osoba fizyczna,
        korzystająca z Usług oferowanych przez Serwisy, które nie
        wymagają założenia Konta.
    </p>
    <p>
        „Wizerunek” – nazwisko lub pseudonim, wizerunek Użytkownika w
        rozumieniu art. 23 KC z dnia 23 kwietnia 1964 r. Kodeks cywilny
        (Dz.U. 1964 nr 16 poz. 93).
    </p>
