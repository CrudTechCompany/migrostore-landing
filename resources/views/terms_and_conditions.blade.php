    <ul>
        <li>aplikowanie profilem</li>
        <li>
            skorzystanie z aplikowania profilem możliwe jest wyłącznie
            po uzupełnieniu przez Użytkownika w profilu danych
            osobowych.
        </li>
        <li>Rekomendowane oferty pracy</li>
    </ul>
    <p>
        Usługodawca informuje Użytkownika – za pomocą specjalnego,
        indywidualnych powiadomień o ofertach pracy publikowanych w
        Serwisie, dopasowanych do preferencji i zainteresowań
        Użytkownika. Powiadomienia tworzoną się dla indywidualnego
        Użytkownika na podstawie analizy jego preferencji i
        zainteresowań, w tym ofert pracy, które Użytkownik wysyłał za
        pośrednictwem aplikacji oraz informacji przetwarzanych w związku
        z innymi Usługami świadczonymi przez Usługodawcę. Rekomendowane
        oferty pracy są prezentowane Użytkownikowi przez Usługodawcę w
        formie powiadomień w aplikacji mobilnej lub za pomocą
        powiadomień wyświetlanych w przeglądarce (zgodnie z ustawieniami
        przeglądarki). Użytkownik może zrezygnować z otrzymywania takich
        powiadomień w Ustawieniach (Prywatność) lub w innym miejscu
        wskazanym w Serwisach
    </p>
    <p>Informowanie o statusie aplikacji</p>
    <p>
        Usługodawca informuje Użytkownika o statusie złożonej aplikacji
        na ofertę pracy opublikowaną w Serwisach, na podstawie
        aktywności pracodawcy, począwszy od momentu odebrania aplikacji
        przez pracodawcę do momentu jej rozpatrzenia. Usługodawca
        udostępnia Użytkownikowi podsumowanie statusów złożonych
        aplikacji o ile pracodawca umożliwi Usługodawcy podanie takich
        informacji. Usługodawca może przekazać Użytkownikowi informacje,
        o których mowa powyżej, w czasie przez siebie wybranym, w
        szczególności za pomocą: specjalnych dedykowanych wiadomości
        e-mail/sms przesyłanych na udostępniony przez Użytkownika numer
        telefonu/adres email w formie powiadomień w aplikacji mobilnej
        lub za pomocą powiadomień wyświetlanych w przeglądarce (zgodnie
        z ustawieniami przeglądarki). Użytkownik może zrezygnować z
        otrzymywania takich powiadomień w Ustawieniach (Prywatność) lub
        w innym miejscu wskazanym w Serwisach
    </p>
    <p>Zapisane oferty pracy</p>
    <p>
        Użytkownik ma możliwość zapisania wybranych przez siebie ofert
        pracy w celu ich wyróżnienia na wynikach wyszukiwania i na
        podglądzie oferty pracy, jak również w celu ułatwienia dostępu
        do nich. Zapisane oferty pracy będą dostępne w dedykowanej
        zakładce w Koncie. Użytkownik ma możliwość zarządzania swoimi
        zapisanymi ofertami pracy za pośrednictwem Konta, w
        szczególności może je usuwać i przechowywać przy nich notatki.
        Użytkownik/Pracodawca zachowuje prawo do usunięcia oferty z
        Zapisanych ofert pracy wraz z terminem zakończenia publikacji
        oferty pracy w Serwisie. Usługodawca może przekazać
        Użytkownikowi informacje o Zapisanych ofertach pracy w formie
        powiadomień w aplikacji mobilnej lub za pomocą powiadomień
        wyświetlanych w przeglądarce (zgodnie z ustawieniami
        przeglądarki). Użytkownik może zrezygnować z otrzymywania takich
        powiadomień w Ustawieniach (Prywatność) lub w innym miejscu
        wskazanym w Serwisach. Usługodawca zachowuje prawo do usunięcia
        oferty z Zapisanych ofert pracy, jeżeli oferta będzie niezgodna
        z prawem, obraźliwa, wulgarna lub Pracodawca nie będzie aktywnym
        podmiotem w CEiDG/KRS
    </p>
