<div>
    <span>§ 11</span>
    <span>
        Rozwiązanie umowy lub odstąpienie od umowy
    </span>
    <p>
        Umowa o świadczenie Usług może być rozwiązana przez którąkolwiek
        ze stron
    </p>
    <p>
        W dowolnym momencie Użytkownik ma prawo rozwiązać umowę o
        świadczenie Usług poprzez samodzielne usunięcie swojego Konta, a
        Użytkownik niezalogowany poprzez zaprzestanie korzystania z
        Usług
    </p>
    <p>
        Usługodawca ma prawo rozwiązać umowę o świadczenie Usług w
        trybie natychmiastowym, w tym usunąć Konto Użytkownika, w
        następujących przypadkach:
    </p>
    <ul>
        <li>
            naruszenia przez Użytkownika lub Użytkownika niezalogowanego
            istotnych postanowień Regulaminu
        </li>
        <li>
            powzięcia przez Usługodawcę uzasadnionych, wiarygodnych
            informacji, że nazwa Konta jest nieprawdziwa, sprzeczna z
            prawem, dobrymi obyczajami, narusza dobra osobiste osób
            trzecich lub uzasadnione interesy Usługodawcy
        </li>
        <li>
            umieszczania w Serwisie lub w Koncie przez Użytkownika lub
            Użytkownika niezalogowanego treści nieprawdziwych,
            niezgodnych z obowiązującymi przepisami prawa, naruszających
            chronione prawem dobra osobiste osób trzecich lub
            uzasadnione interesy Usługodawcy
        </li>
        <li>
            wykorzystywania przez Użytkownika lub Użytkownika
            niezalogowanego Usług niezgodnie ze ich przeznaczeniem
        </li>
    </ul>
    <p>
        O rozwiązaniu umowy o świadczenie Usług Usługodawca poinformuje
        Użytkownika lub Użytkownika niezalogowanego (jeśli istnieje taka
        techniczna możliwość) najpóźniej w terminie 24 godzin od dnia
        rozwiązania
    </p>
    <p>
        Usługodawca zastrzega sobie prawo do odmowy świadczenia Usług na
        rzecz Użytkownika lub Użytkownika niezalogowanego, w tym
        usunięcia Konta Użytkownika, jeżeli zostało ono założone po raz
        kolejny po usunięciu Konta przez Usługodawcę, wynikającym z
        naruszenia Regulaminu
    </p>
</div>
<div>
    <span>§ 12</span>
    <span>Postaniwienia końcowe</span>
    <p>
        Regulamin obowiązuje bezterminowo. Usługodawca może w każdym
        czasie z ważnych przyczyn, w tym wskazanych w zakresie
        odpowiedzialności podmiotu, zakończyć świadczenie Usług (w tym
        usługi Konta) za pośrednictwem Serwisu, przesyłając o tym
        informację za pośrednictwem poczty elektronicznej lub sms
    </p>
    <p>
        Usługodawca zastrzega sobie prawo do zmiany Regulaminu z niżej
        wskazanych, ważnych przyczyn:
    </p>
    <ul>
        <li>
            jeżeli zmiana Regulaminu jest konieczna ze względu na zmianę
            przepisów powszechnie obowiązującego prawa
        </li>
        <li>
            realizacji obowiązku wynikającego z prawomocnego orzeczenia
            sądu lub decyzji organów administracji
        </li>
        <li>
            zmian wynikających ze względów bezpieczeństwa, w tym
            mających na celu uniemożliwienie korzystania z Serwisów, w
            sposób sprzeczny z przepisami prawa lub z Regulaminem
        </li>
        <li>
            wprowadzenia istotnych zmian w funkcjonowaniu Serwisu oraz
            którejkolwiek z Usług, w tym związanych z postępem
            technicznym lub technologicznym, obejmujących zmiany w
            systemach Usługodawcy
        </li>
    </ul>
    <p>
        Informacja o zmianie Regulaminu zostanie przekazana za
        pośrednictwem emaila lub informacji push na telefonu
        udostępniony przez Użytkownika lub poprzez przekazanie jasnej i
        wyraźnej informacji na stronach Serwisu, w obu przypadkach, w
        terminie 7 dni od momentu opublikowania. Zmiany wchodzą w życie
        w dacie wskazanej w informacji, o której mowa w niniejszym
        ustępie
    </p>
    <p>
        Regulamin oraz umowy o świadczenie Usług podlegają prawu
        polskiemu
    </p>
    <p>
        Wszelkie spory związane ze świadczeniem Usług zgodnie z
        Regulaminem będzie rozstrzygał polski sąd powszechny
    </p>
    <p>
        Niezależnie od postanowień Regulaminu istnieje możliwości
        skorzystania przez Użytkownika i Użytkownika niezalogowanego z
        pozasądowych sposobów rozpatrywania reklamacji i dochodzenia
        roszczeń. Właściwe procedury w tym zakresie dostępne są m.in. w
        Urzędzie Ochrony Konkurencji i Konsumentów oraz na stronie
        www.uokik.gov.pl
    </p>
    <p>
        W sprawach nieuregulowanych w Regulaminie zastosowanie mają
        przepisy Ustawy o świadczeniu usług drogą elektroniczną, Kodeksu
        cywilnego i inne bezwzględnie obowiązujące przepisy prawa
    </p>
    <p>Załączniki stanowią integralną część Regulaminu</p>
